Route::get('/', function () {
    return view('welcome');
});

// Rotte per l'area admin
Route::middleware(['auth', 'verified'])->prefix('admin')->name('admin.')->group(function () {
    Route::get('/', [DashboardController::class, 'index'])->name('dashboard');

    // Rotte per la gestione dei books
    Route::resource('books', BookController::class)->parameters(['books' => 'book:slug']);

    Route::get('recycle', [BookController::class, 'recycle'])->name('books.recycle');
    Route::get('books/restore/{slug}', [BookController::class, 'restore'])->name('books.restore');
});


Route::middleware('auth')->group(function () {
    Route::get('/profile', [ProfileController::class, 'edit'])->name('profile.edit');
    Route::patch('/profile', [ProfileController::class, 'update'])->name('profile.update');
    Route::delete('/profile', [ProfileController::class, 'destroy'])->name('profile.destroy');
});

require __DIR__ . '/auth.php';
